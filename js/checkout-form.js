function openSesame() {
  var x = document.getElementById("creditCardInfo");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "block";
  }

}

function closeSesame() {
  var x = document.getElementById("creditCardInfo");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "none";
  }
}
